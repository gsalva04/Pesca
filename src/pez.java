public class pez {

    //Atributos
    String nombre;
    double peso;

    //Contructores
    public pez() {
    }
    public pez(String nombre, double peso) {
        this.nombre = nombre;
        this.peso = peso;
    }

    //Getters y Setters
    public String getNombre() {
        return nombre;
    }
    public double getPeso() {
        return peso;
    }

}
